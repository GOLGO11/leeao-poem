function csvEscape(value) {
  const text = String(value ?? "");
  return /[",\n\r]/.test(text) ? `"${text.replace(/"/g, '""')}"` : text;
}

function row(chapter, file, lineStart, lineEnd, quoteText, category, sourceOrigin, usageSummary, notes = "") {
  return {
    id: "",
    book,
    chapter,
    source_file: file,
    line_start: lineStart,
    line_end: lineEnd,
    quote_text: quoteText,
    category,
    source_or_origin: sourceOrigin,
    summary: usageSummary,
    notes,
  };
}

function readSourceLines(file) {
  return sourceDecoder.decode(fs.readFileSync(path.join(sourceRoot, file))).split(/\r?\n/);
}

function cleanLine(line) {
  return String(line ?? "").replace(/^[\s　]+/, "").replace(/[\s　]+$/, "");
}

function decodeSeedText(text) {
  return String(text ?? "").replace(/\\n/g, "\n");
}

function parseManualRows() {
  return manualSeeds
    .join("\n")
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean)
    .filter((line) => !line.startsWith("#"))
    .map((line, index) => {
      const cells = line.split("\t");
      if (cells.length < 8) {
        throw new Error(`Invalid manual seed at line ${index + 1}: expected at least 8 tab-separated fields`);
      }
      const [chapter, file, lineStart, lineEnd, quoteText, category, sourceOrigin, summary, notes = ""] = cells;
      return row(
        chapter,
        file,
        Number(lineStart),
        Number(lineEnd),
        decodeSeedText(quoteText),
        category,
        sourceOrigin,
        decodeSeedText(summary),
        decodeSeedText(notes),
      );
    });
}

function parseCoupletRows() {
  const file = "038.联语对比举例.txt";
  const lines = readSourceLines(file);
  const rows = [];

  for (let index = 0; index < lines.length; index += 1) {
    const heading = cleanLine(lines[index]);
    if (!/^（.+）.+(?:联|：|:)[：:；;]?$/.test(heading)) continue;

    const first = cleanLine(lines[index + 2]);
    const second = cleanLine(lines[index + 4]);
    if (!first || !second || first.startsWith("（") || second.startsWith("（")) continue;

    const sourceOrigin = heading.replace(/[：:；;]$/, "");
    rows.push(
      row(
        "联语对比举例",
        file,
        index + 3,
        index + 5,
        `${first}${second}`,
        "联语",
        sourceOrigin,
        "李敖在《联语对比举例》中列作联语对比样本。",
        "首轮程序提取：按题注后的上下联合并。",
      ),
    );
  }

  return rows;
}

