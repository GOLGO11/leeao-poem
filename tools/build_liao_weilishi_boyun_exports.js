function validateRows(rows) {
  const ids = new Set();
  const duplicateIds = new Set();
  for (const record of rows) {
    if (ids.has(record.id)) {
      duplicateIds.add(record.id);
    }
    ids.add(record.id);
    for (const column of columns) {
      if (!(column in record)) {
        throw new Error(`Missing column ${column} in ${record.id}`);
      }
    }
    const lines = readLines(record.source_file);
    const sourceQuote = lines.slice(record.line_start - 1, record.line_end).join("\n");
    if (!comparable(sourceQuote).includes(comparable(record.quote_text))) {
      throw new Error(
        `Quote text not found for ${record.id} ${record.source_file}:${record.line_start}-${record.line_end}\n` +
          `quote=${record.quote_text}\nsource=${sourceQuote}`,
      );
    }
  }
  if (duplicateIds.size > 0) {
    throw new Error(`Duplicate ids: ${[...duplicateIds].join(", ")}`);
  }
}

function writeCsv(rows, csvPath) {
  const lines = [
    columns.join(","),
    ...rows.map((record) => columns.map((column) => csvEscape(record[column])).join(",")),
  ];
  fs.writeFileSync(csvPath, `${lines.join("\r\n")}\r\n`, "utf8");
}

