const csvEscape = (value) => {
  const text = value == null ? "" : String(value);
  return /[",\r\n]/.test(text) ? `"${text.replace(/"/g, '""')}"` : text;
};

const outDir = path.join(process.cwd(), "exports");
fs.mkdirSync(outDir, { recursive: true });

const outputRows = [...rows].sort(
  (a, b) =>
    a.source_file.localeCompare(b.source_file, "zh-Hans-CN") ||
    Number(a.line_start) - Number(b.line_start) ||
    a.id.localeCompare(b.id),
);

const csvLines = [
  columns.join(","),
  ...outputRows.map((record) => columns.map((column) => csvEscape(record[column])).join(",")),
];

const csvPath = path.join(outDir, "李敖议坛哀思录_诗文格言歌谣引用.csv");
fs.writeFileSync(csvPath, `\uFEFF${csvLines.join("\r\n")}\r\n`, "utf8");

const txt = [];
txt.push("《李敖议坛哀思录》诗文格言歌谣引用摘录");
txt.push(`生成日期：${generatedDate}`);
txt.push(`条目数：${outputRows.length}`);
txt.push("");
txt.push("说明：本轮为初提取，按新口径收诗文、古文名句、格言、俗谚、歌谣/歌词、联语及宗教/哲学经典句；普通政论语录、公文条文、议场口号、政策判断和李敖自作诗不收。");
txt.push("");

let currentChapter = "";
for (const record of outputRows) {
  if (record.chapter !== currentChapter) {
    currentChapter = record.chapter;
    txt.push(`## ${currentChapter}`);
  }

  txt.push(`${record.id}｜${record.category}｜${record.source_file}:${record.line_start}-${record.line_end}`);
  txt.push(`引用：${record.quote_text}`);
  txt.push(`出处线索：${record.source_or_origin}`);
  txt.push(`摘要：${record.summary}`);
  if (record.notes) txt.push(`备注：${record.notes}`);
  txt.push("");
}

const txtPath = path.join(outDir, "李敖议坛哀思录_诗文格言歌谣引用.txt");
fs.writeFileSync(txtPath, `\uFEFF${txt.join("\r\n")}\r\n`, "utf8");

const duplicateIds = outputRows
  .map((record) => record.id)
  .filter((id, index, ids) => ids.indexOf(id) !== index);

