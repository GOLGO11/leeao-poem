function tsvEscape(value) {
  return String(value ?? "").replace(/\r?\n/g, "\\n");
}

function normalizeForSearch(text) {
  return String(text ?? "")
    .replace(/\r\n/g, "\n")
    .replace(/\r/g, "\n")
    .replace(/\uFEFF/g, "")
    .trim();
}

const validationErrors = [];
for (const item of data) {
  const sourceText = sourceTexts.get(item.source_file);
  if (!sourceText) {
    validationErrors.push({ id: item.id, error: "missing-source-file", source_file: item.source_file });
    continue;
  }
  const lines = sourceText.split(/\r?\n/);
  const snippet = lines.slice(item.line_start - 1, item.line_end).join("\n");
  if (!normalizeForSearch(snippet).includes(normalizeForSearch(item.quote_text))) {
    validationErrors.push({
      id: item.id,
      error: "quote-not-found-in-line-range",
      source_file: item.source_file,
      line_start: item.line_start,
      line_end: item.line_end,
      quote_text: item.quote_text,
      snippet,
    });
  }
}

if (validationErrors.length) {
  console.error(JSON.stringify(validationErrors.slice(0, 20), null, 2));
  throw new Error(`Validation failed: ${validationErrors.length} errors`);
}

