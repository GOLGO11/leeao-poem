function validateRows(rows) {
  const ids = new Set();
  for (const row of rows) {
    if (ids.has(row.id)) throw new Error(`Duplicate id ${row.id}`);
    ids.add(row.id);

    const lines = readLines(row.source_file);
    const sourceText = lines.slice(row.line_start - 1, row.line_end).join("\n");
    if (
      !sourceText.includes(row.quote_text) &&
      !comparable(sourceText).includes(comparable(row.quote_text))
    ) {
      throw new Error(`Quote not found for ${row.id} ${row.source_file}:${row.line_start}-${row.line_end}`);
    }
  }
}

function writeCsv(rows, csvPath) {
  const columns = [
    "id",
    "book",
    "chapter",
    "source_file",
    "line_start",
    "line_end",
    "quote_text",
    "category",
    "source_or_origin",
    "summary",
    "notes",
  ];
  const lines = [
    columns.join(","),
    ...rows.map((record) => columns.map((column) => csvEscape(record[column])).join(",")),
  ];
  fs.writeFileSync(csvPath, `\uFEFF${lines.join("\r\n")}\r\n`, "utf8");
}

function writeTxt(rows, txtPath) {
  const lines = [
    `《${book}》诗文格言歌谣引用`,
    `生成日期：${generatedDate}`,
    `条目数：${rows.length}`,
    "",
  ];

  for (const record of rows) {
    lines.push(`${record.id}｜${record.chapter}｜${record.source_file}:${record.line_start}-${record.line_end}`);
    lines.push(`类别：${record.category}`);
    lines.push(`来源：${record.source_or_origin}`);
    lines.push("原文：");
    lines.push(record.quote_text);
    lines.push(`说明：${record.summary}`);
    if (record.notes) lines.push(`备注：${record.notes}`);
    lines.push("");
  }

  fs.writeFileSync(txtPath, `\uFEFF${lines.join("\r\n")}`, "utf8");
}

function collectCandidates(rows) {
  const selectedKeys = rows.map((record) => ({
    file: record.source_file,
    start: Number(record.line_start),
    end: Number(record.line_end),
  }));
  const candidates = [];
  let totalLines = 0;
  let riskLines = 0;

  for (const file of sourceFiles()) {
    const lines = readLines(file);
    totalLines += lines.length;
    lines.forEach((line, index) => {
      const text = line.trim();
      if (!text) return;
      if (/李敖影印|李敖数字博物馆|leeaoweb|leeao\.net|wjm_tcy|QQ群|油管|抖音/.test(text)) return;
      const risk = modernPoliticalPattern.test(text);
      if (risk) riskLines += 1;
      if (!candidatePattern.test(text) && !risk) return;
      const lineNumber = index + 1;
      candidates.push({
        source_file: file,
        chapter: chapterFromFile(file),
        line: lineNumber,
        selected: selectedKeys.some((item) => item.file === file && lineNumber >= item.start && lineNumber <= item.end),
        risk,
        text,
        context: lines
          .slice(Math.max(0, index - 1), Math.min(lines.length, index + 2))
          .map((item) => item.trim())
          .filter(Boolean)
          .join(" / "),
      });
    });
  }

  return { candidates, totalLines, riskLines };
}

