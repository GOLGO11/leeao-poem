function validateRows(rows) {
  const seen = new Set();
  const errors = [];
  for (const row of rows) {
    if (seen.has(row.id)) errors.push(`Duplicate id ${row.id}`);
    seen.add(row.id);

    const lines = readLines(row.source_file);
    const sourceText = lines.slice(row.line_start - 1, row.line_end).join("\n");
    if (
      !sourceText.includes(row.quote_text) &&
      !comparable(sourceText).includes(comparable(row.quote_text))
    ) {
      errors.push(
        `${row.id || "(pending id)"} quote not found in ${row.source_file}:${row.line_start}-${row.line_end}: ${row.quote_text}`,
      );
    }
  }
  if (errors.length) {
    throw new Error(errors.join("\n"));
  }
}

